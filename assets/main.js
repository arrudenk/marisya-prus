// Ukrainian texts are baked into the HTML; snapshot them for toggling back
I18N.uk = {};
document.querySelectorAll('[data-i18n]').forEach(el => {
  I18N.uk[el.dataset.i18n] = el.textContent;
});
I18N.uk.meta_title = document.title;

function setLang(lang) {
  const dict = I18N[lang] || I18N.uk;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const t = dict[el.dataset.i18n];
    if (t !== undefined) el.textContent = t;
  });
  document.documentElement.lang = lang;
  document.title = dict.meta_title;
  document.querySelectorAll('.lang-toggle button').forEach(b =>
    b.classList.toggle('active', b.dataset.lang === lang));
  try { localStorage.setItem('lang', lang); } catch (e) {}
}

document.querySelectorAll('.lang-toggle button').forEach(btn =>
  btn.addEventListener('click', () => setLang(btn.dataset.lang)));

let saved = null;
try { saved = localStorage.getItem('lang'); } catch (e) {}
if (saved === 'en') setLang('en');

/* ===== Lightbox ===== */
const lightbox = document.getElementById('lightbox');
const lightboxImg = lightbox.querySelector('img');
const lightboxFrame = lightbox.querySelector('.lightbox-frame');

function openLightbox() {
  lightbox.hidden = false;
  document.body.style.overflow = 'hidden';
}

document.querySelectorAll('.grid figure img, .split figure img').forEach(img => {
  img.addEventListener('click', () => {
    lightboxImg.src = img.src;
    lightboxImg.alt = img.alt;
    lightboxImg.hidden = false;
    openLightbox();
  });
});

/* Video / PDF links → theatre-mode iframe */
document.querySelectorAll('a[data-preview]').forEach(link => {
  link.addEventListener('click', e => {
    e.preventDefault();
    lightboxFrame.src = link.dataset.preview;
    lightboxFrame.title = link.textContent.trim();
    lightboxFrame.hidden = false;
    lightboxImg.hidden = true;
    lightbox.classList.add('lb-' + link.dataset.embed);
    openLightbox();
  });
});

function closeLightbox() {
  lightbox.hidden = true;
  lightboxImg.src = '';
  lightboxImg.hidden = false;
  lightboxFrame.src = ''; // unloads the Drive player, stops playback
  lightboxFrame.hidden = true;
  lightbox.classList.remove('lb-video', 'lb-pdf');
  document.body.style.overflow = '';
}
lightbox.addEventListener('click', closeLightbox);
document.addEventListener('keydown', e => { if (e.key === 'Escape' && !lightbox.hidden) closeLightbox(); });

